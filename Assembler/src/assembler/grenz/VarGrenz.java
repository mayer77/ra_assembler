package assembler.grenz;

/**
 *
 * @author mmaye
 */
public class VarGrenz {

    private String ma;
    private String label;

    public VarGrenz(String ma, String label) {
        this.ma = ma;
        this.label = label;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

}
